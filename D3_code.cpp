float calcDistance(float rssi) {
  return pow(10, (REF_RSSI - rssi) / (10.0 * PATH_LOSS));
}

float normalizeAngle(float diff) {
  diff = fmod(diff, 360);
  if (diff > 180) diff -= 360;
  else if (diff < -180) diff += 360;
  return diff;
}

const unsigned char* getArrowBitmap(float angle) {
  if (angle >= -22.5 && angle < 22.5) return epd_bitmap_up;
  else if (angle >= 22.5 && angle < 67.5) return epd_bitmap_up_right;
  else if (angle >= 67.5 && angle < 112.5) return epd_bitmap_right;
  else if (angle >= 112.5 && angle < 157.5) return epd_bitmap_down_right;
  else if (angle >= 157.5 || angle < -157.5) return epd_bitmap_down;
  else if (angle >= -157.5 && angle < -112.5) return epd_bitmap_down_left;
  else if (angle >= -112.5 && angle < -67.5) return epd_bitmap_left;
  else if (angle >= -67.5 && angle < -22.5) return epd_bitmap_up_left;
  return epd_bitmap_direction_none;
}

void alertOutOfRange(String name, float dist) {
  display.clearDisplay();
  display.setTextSize(1);
  display.setCursor(0, 0);
  display.println(name + " out of range!");
  display.setCursor(0, 20);
  display.print("Distance: ");
  display.print(dist, 1);
  display.println(" m");
  display.display();
  digitalWrite(motorPin, HIGH);
  delay(1000);
  digitalWrite(motorPin, LOW);
}

void alertSOS() {
  display.clearDisplay();
  display.setTextSize(2);
  display.setCursor(0, 20);
  display.println("!!! SOS !!!");
  display.display();
  digitalWrite(motorPin, HIGH);
  delay(1500);
  digitalWrite(motorPin, LOW);
}

WiFiClient client;

void setup() {
  Serial.begin(115200);
  Wire.begin(21, 22);
  compass.init();
  pinMode(sosPin, INPUT_PULLUP);
  pinMode(motorPin, OUTPUT);

  // Initialize OLED
  if (!display.begin(SSD1306_SWITCHCAPVCC, 0x3C)) {
    Serial.println("OLED fail");
    while (true);
  }
  
  // Show boot logo
  display.clearDisplay();
  display.drawBitmap(0, 0, LOGOUntitled_design__1_, 128, 64, SSD1306_WHITE);
  display.display();
  delay(2000);

  // Connect to WiFi
  display.clearDisplay();
  display.setTextSize(1);
  display.setCursor(0, 0);
  display.println("Connecting WiFi...");
  display.display();
  
  WiFi.begin(ssid, password);
  while (WiFi.status() != WL_CONNECTED) {
    delay(500);
    Serial.print(".");
  }
  
  display.clearDisplay();
  display.setCursor(0, 0);
  display.print("IP: ");
  display.println(WiFi.localIP());
  display.display();
  delay(1000);

  // Initialize BLE scanner
  BLEDevice::init("");
  pBLEScan = BLEDevice::getScan();
  pBLEScan->setActiveScan(true);
  pBLEScan->setInterval(50);
  pBLEScan->setWindow(49);
}

void loop() {
  // 1. Check for SOS button press
  if (digitalRead(sosPin) == LOW) {
    alertSOS();
    if (client.connected()) {
      client.print("SOS," + WiFi.macAddress() + "\n");
    }
  }

  // 2. Perform BLE scan
  BLEScanResults scanResults = pBLEScan->start(1, false);
  
  // Process scan results
  for (int i = 0; i < 2; i++) { // For D1 and D4
    peers[i].connected = false;
    
    for (int j = 0; j < scanResults.getCount(); j++) {
      BLEAdvertisedDevice device = scanResults.getDevice(j);
      String deviceMac = device.getAddress().toString().c_str();
      
      if (deviceMac == peers[i].mac) {
        peers[i].rssi = device.getRSSI();
        peers[i].distance = calcDistance(peers[i].rssi);
        peers[i].connected = true;
        break;
      }
    }
  }
  pBLEScan->clearResults();

  // 3. Get self heading
  compass.read();
  int selfHeading = compass.getAzimuth();

  // 4. Connect to D1 and send data
  if (!client.connected()) {
    if (client.connect(D1_IP, port)) {
      Serial.println("Connected to D1 server");
    }
  }

  if (client.connected()) {
    String packet = WiFi.macAddress() + "," + String(selfHeading) + "\n";
    client.print(packet);
    Serial.println("Sent: " + packet);
  }

  // 5. Display peer information
  for (int i = 0; i < 2; i++) {
    if (peers[i].connected) {
      // Calculate relative direction
      float diff = normalizeAngle(peers[i].heading - selfHeading);
      const unsigned char* arrow = getArrowBitmap(diff);

      // Display on OLED
      display.clearDisplay();
      display.setTextSize(1);
      display.setCursor(0, 0);
      display.println(peers[i].name);
      display.setTextSize(2);
      display.setCursor(0, 20);
      display.print(peers[i].distance, 1);
      display.print(" m");
      display.drawBitmap(86, 18, arrow, 32, 30, SSD1306_WHITE);
      display.display();

      // Check distance thresholds
      float threshold = (peers[i].name == "D1") ? 10.0 : 4.0;
      if (peers[i].distance > threshold) {
        alertOutOfRange(peers[i].name, peers[i].distance);
      }
    } else {
      display.clearDisplay();
      display.setCursor(0, 0);
      display.println(peers[i].name + " not found");
      display.display();
    }
    delay(1000);
  }

  delay(1000); // Main loop delay
}